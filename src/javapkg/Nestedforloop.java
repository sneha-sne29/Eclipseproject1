package javapkg;

public class Nestedforloop {

	public static void main(String[] args) {
		
		   /* + + + +
		    * + + + +          i=4,j=4
		    *  + + + +
		    *   + + + +
		    */
		for(int i=1;i<=4;i++)
		{
			for(int j=1;j<=4;j++)
			{
				System.out.print(" + ");
			}
			System.out.println();
		}

	}

}
